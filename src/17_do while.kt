fun main() {
    // WHILE
    
    var i = 5
    while(i <= 4) {
        println("Hello, World!")
        i++
    }
    println("Looping berhenti $i")
    
    // DO WHILE
    var j = 5
    do {
        println("Hallo, Dunia Fatamorgana!")
        j++
    } while (j <= 4)
    println("Looping berhenti $j")
}